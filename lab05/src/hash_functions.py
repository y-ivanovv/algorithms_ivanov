def hash_sum(s: str) -> int:
    """
    Простая хеш-функция — сумма кодов символов.
    Особенности:
        - Очень слабая, плохо распределяет данные.
        - Много коллизий на похожих строках.
    """
    h = 0
    for char in s:
        h += ord(char)
    return h


def hash_poly(s: str, base: int = 257, mod: int = 2**64) -> int:
    """
    Полиномиальный хеш.
    Особенности:
        - Хорошее распределение.
        - Быстрый.
        - Стандарт в алгоритмах обработки строк.
    """
    h = 0
    for char in s:
        h = (h * base + ord(char)) % mod
    return h


def hash_djb2(s: str) -> int:
    """
    DJB2 — популярная хеш-функция Дэна Бернштейна.
    Характеристики:
        - Хорошее распределение.
        - Низкая вероятность коллизий.
        - Быстрая.
    """
    h = 5381
    for char in s:
        h = ((h << 5) + h) + ord(char)
    return h & 0xFFFFFFFFFFFFFFFF
